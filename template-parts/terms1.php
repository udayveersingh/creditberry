
<p class="text-center"><b>NOTICE TO USERS OF CONSUMER REPORTS: <br />
        OBLIGATIONS OF USERS UNDER THE FCRA</b></p>
<p>The Fair Credit Reporting Act (FCRA), 15 U.S.C. &sect;1681-1681y, requires that this notice be provided to inform users of consumer reports of their legal obligations. State law may impose additional requirements. The text of the FCRA is set forth in full at the Bureau of Consumer Financial Protection's (CFPB) website at <a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>. At the end of this document is a list of United States Code citations for the FCRA. Other information about user duties is also available at the CFPB's website. <b>Users must consult the relevant provisions of the FCRA for details about their obligations under the FCRA. </b> </p>
<p>The first section of this summary sets forth the responsibilities imposed by the FCRA on all users of consumer reports. The subsequent sections discuss the duties of users of reports that contain specific types of information, or that are used for certain purposes, and the legal consequences of violations. If you are a furnisher of information to a consumer reporting agency (CRA), you have additional obligations and will receive a separate notice from the CRA describing your duties as a furnisher.</p>
<ol type="I"><li><p><b>OBLIGATIONS OF ALL USERS OF CONSUMER REPORTS<br />
            </b><br />
        </p>
    </li>
</ol> <ol type="A"><li><p><b>Users Must Have a Permissible Purpose</b></p>
    </li>
</ol><p><br />
</p>
<p>Congress has limited the use of consumer reports to protect consumers' privacy. All users must have a permissible purpose under the FCRA to obtain a consumer report. Section 604 contains a list of the permissible purposes under the law. These are:</p>
<p><br />
</p>
<ul>
    <li><p>As ordered by a court or federal grand jury subpoena. Section 604(a)(1)</p>
    </li>
    <li><p>As instructed by the consumer in writing. Section 604(a)(2) </p>

    </li>
    <li><p>For the extension of credit as a result of an application from a consumer, or the review or collection of a consumer's account. Section 604(a)(3)(A)</p>

    </li>
    <li><p>For the underwriting of insurance as a result of an application from a consumer. Section 604(a)(3)(C) and 604(b)</p>

    </li>
    <li><p>When there is a legitimate business need, in connection with a business transaction that is <b>initiated</b> by the consumer. Section 604 (a)(3)(F)(i)</p>

    </li>
    <li><p>To review a consumer's account to determine whether the consumer continues to meet the terms of the account. Section 604(a)(3)(F)(ii)</p>

    </li>
    <li><p>To determine a consumer's eligibility for a license or other benefit granted by a governmental instrumentality required by law to consider an applicant's financial responsibility or status. Section 604(a)(3)(D)</p>

    </li>
    <li><p>For use by a potential investor or servicer, or current insurer, in a valuation or assessment of the credit or prepayment risks associated with an existing credit obligation. Section 604(a)(3)(E)</p>

    </li>
    <li><p>For use by state and local officials in connection with the determination of child support payments, or modifications and enforcement thereof. Sections 604(a)(4) and 604(a)(5)</p>

    </li>
</ul>

<p><br />
</p>

<p>In addition, creditors and insurers may obtain certain consumer report information for the purpose of making "prescreened" unsolicited offers of credit or insurance. Section 604(c). The particular obligations of users of"prescreened" information are described in Section VII below.</p>

<ol start="2" type="A"><li><p><b>Users Must Provide Certifications</b></p>

    </li>
</ol><p><br />
</p>

<p>Section 604(f) prohibits any person from obtaining a consumer report from a consumer reporting agency (CRA) unless the person has certified to the CRA the permissible purpose(s) for which the report is being obtained and certifies that the report will not be used for any other purpose. </p>

<ol start="3" type="A"><li><p><b>Users Must Notify Consumers When Adverse Actions Are Taken</b></p>

    </li>
</ol><p>The Term"adverse action" is defined very broadly by Section 603."Adverse actions" include all business, credit, and employment actions affecting consumers that can be considered to have a negative impact as defined by Section 603(k) of the FCRA - such as denying or canceling credit or insurance, or denying employment or promotion. No adverse action occurs in a credit transaction where the creditor makes a counteroffer that is accepted by the consumer. </p>

<ol><li><p><b>Adverse Actions Based on Information Obtained From a CRA</b></p>

    </li>
</ol><p><br />
</p>

<p>If a user takes any type of adverse action as defined by the FCRA that is based at least in part on information contained in a consumer report, Section 615(a) requires the user to notify the consumer. The notification may be done in writing, orally, or by electronic means. It must include the following: </p>

<ul>
    <li><p>The name, address, and telephone number of the CRA (including a toll-free telephone number, if it is a nationwide CRA) that provided the report. </p>

    </li>
    <li><p>A statement that the CRA did not make the adverse decision and is not able to explain why the decision was made. </p>

    </li>
    <li><p>A statement setting forth the consumer's right to obtain a free disclosure of the consumer's file from the CRA if the consumer makes a request within 60 days.</p>

    </li>
    <li><p>A statement setting forth the consumer's right to dispute directly with the CRA the accuracy or completeness of any information provided by the CRA. </p>

    </li>
</ul>

<p><br />
</p>

<ol start="2"><li><p><b>Adverse Actions Based on Information Obtained From Third Parties Who Are Not Consumer Reporting Agencies</b></p>

    </li>
</ol><p>If a person denies (or increases the charge for) credit for personal, family, or household purposes based either wholly or partly upon information from a person other than a CRA, and the information is the type of consumer information covered by the FCRA, Section 615(b)(1) requires that the user clearly and accurately disclose to the consumer his or her right to be told the nature of the information that was relied upon if the consumer makes a written request within 60 days of notification. The user must provide the disclosure within a reasonable period of time following the consumer's written request. </p>

<ol start="3"><li><p><b>Adverse Actions Based on Information Obtained From Affiliates</b></p>

    </li>
</ol><p>If a person takes an adverse action involving insurance, employment, or a credit transaction initiated by the consumer, based on information of the type covered by the FCRA, and this information was obtained from an entity affiliated with the user of the information by common ownership or control, Section 615(b)(2) requires the user to notify the consumer of the adverse action. The notice must inform the consumer that he or she may obtain a disclosure of the nature of the information relied upon by making a written request within 60 days of receiving the adverse action notice. If the consumer makes such a request, the user must disclose the nature of the information not later than 30 days after receiving the request. If consumer report information is shared among affiliates and then used for an adverse action, the user must make an adverse action disclosure as set forth in I.C. 1 above.</p>

<ol start="4" type="A"><li><p><b>Users Have Obligations When Fraud and Active Duty Military Alerts are in Files</b></p>

    </li>
</ol><p>When a consumer has placed a fraud alert, including one relating to identify theft, or an active duty military alert with a nationwide consumer reporting agency as defined in Section 603(p) and resellers, Section 605A(h) imposes limitations on users of reports obtained from the consumer reporting agency in certain circumstances, including the establishment of a new credit plan and the issuance of additional credit cards. For initial fraud alerts and active duty alerts, the user must have reasonable policies and procedures in place to form a belief that the user knows the identity of the applicant or contact the consumer at a telephone number specified by the consumer; in the case of extended fraud alerts, the user must contact the consumer in accordance with the contact information provided in the consumer's alert. </p>

<ol start="5" type="A"><li><p><b>Users Have Obligations When Notified of an Address Discrepancy</b></p>

    </li>
</ol><p>Section 605(h) requires nationwide CRAs, as defined in section 603(p), to notify users that request reports when the address for a consumer provided by the user in requesting the report is substantially different from the addresses in the consumer's file. When this occurs, users must comply with regulations specifying the procedures to be followed. Federal regulations are available at <a href="http://www.consumerfinance.gov/learnmore/">www.consumerfinance.gov/learnmore/</a>. </p>

<ol start="6" type="A"><li><p><b>Users Have Obligations When Disposing of Records</b></p>

    </li>
</ol><p>Section 628 requires that all users of consumer report information have in place procedures to properly dispose of records containing this information. The Consumer Financial Protection Bureau, the Securities and Exchange Commission, and the banking and credit union regulators have issued regulations covering disposal. </p>

<ol start="2" type="I"><li><p><b>CREDITORS MUST MAKE ADDITIONAL DISCLOSURES</b></p>

    </li>
</ol><p>If a person uses a consumer report in connection with an application for, or a grant, extensions, or provision of, credit to a consumer on material terms that are materially less favorable than the most favorable terms available to a substantial proportion of consumers from or through that person, based in whole or in part on a consumer report, the person must provide a risk-based pricing notice to the consumer in accordance with regulations prescribed by the Consumer Financial Protection Bureau.</p>

<p>Section 609(g) requires a disclosure by all persons that make or arrange loans secured by residential real property (one to four units) and that use credit scores. These persons must provide credit scores and other information about credit scores to applicants, including the disclosure set forth in Section 609(g)(1)(D) ("Notice to the Home Loan Applicant").</p>

<ol start="2" type="I"><li><p><b>OBLIGATIONS OF USERS WHEN CONSUMER REPORTS ARE OBTAINED FOR EMPLOYMENT PURPORSES. </b> </p>

        <ol type="A"><li><p><b>Employment Other Than in the Trucking Industry</b></p>

            </li>
        </ol> </li>
</ol><p>If information from a CRA is used for employment purposes, the user has specific duties, which are set forth in Section 604(b) of the FCRA. The user must:</p>

<ul>
    <li><p>Make a clear and conspicuous written disclosure to the consumer before the report is obtained, in a document that consists solely of the disclosure, that a consumer report may be obtained.</p>

    </li>
    <li><p>Obtain from the consumer prior written authorization. Authorization to access reports during the term of employment may be obtained at the time of employment.</p>

    </li>
    <li><p>Certify to the CRA that the above steps have been followed, that the information being obtained will not be used in violation of any federal or state equal opportunity law or regulation, and that, if any adverse action is to be taken based on the consumer report, a copy of the report and a summary of the consumer's rights will be provided to the consumer.</p>

    </li>
    <li><p><b>Before </b>taking an adverse action, the user must provide a copy of the report to the consumer as well as the summary of consumer's rights. (The user should receive this summary from the CRA.) A Section 615(a) adverse action notice should be sent after the adverse action is taken.</p>

    </li>
</ul>

<p><br />
</p>

<p>An adverse action notice also is required in employment situations if credit information (other than transactions and experience data) obtained from an affiliate is used to deny employment. Section 615(b)(2)</p>

<p><br />
</p>

<p><br />
</p>

<p>The procedures for investigative consumer reports and employee misconduct investigations are set forth below.</p>

<p><br />
</p>

<ol start="2" type="A"><li><p><b>Employment in the Trucking Industry</b></p>

    </li>
</ol><p>Special rules apply for truck drivers where the only interaction between the consumer and the potential employer is by mail, telephone, or computer. In this case, the consumer may provide consent orally or electronically, and an adverse action may be made orally, in writing, or electronically. The consumer may obtain a copy of any report relied upon by the trucking company by contacting the company.</p>

<ol start="4" type="I"><li><p><b>OBLIGATIONS WHEN INVESTIGATIVE CONSUMER REPORTS ARE USED</b></p>

    </li>
</ol><p>Investigative consumer reports are a special type of consumer report in which information about a consumer's character, general reputation, personal characteristics, and mode of living is obtained through personal interviews by an entity or person that is a consumer reporting agency. Consumers who are the subjects of such reports are given special rights under the FCRA. If a user intends to obtain an investigative consumer report, Section 606 requires the following:</p>

<ul>
    <li><p>The user must disclose to the consumer that an investigative consumer report may be obtained. This must be done in a written disclosure that is mailed, or otherwise delivered, to the consumer at some time before or not later than three days after the date on which the report was first requested. The disclosure must include a statement informing the consumer of his or her right to request additional disclosures of the nature and scope of the investigation as described below, and the summary of consumer rights required by Section 609 of the FCRA. (The summary of consumer rights will be provided by the CRA that conducts the investigation.)</p>

    </li>
    <li><p>The user must certify to the CRA that the disclosures set forth above have been made and that the user will make the disclosure described below.</p>

    </li>
    <li><p>Upon the written request of a consumer made within a reasonable period of time after the disclosures required above, the user must make a complete disclosure of the nature and scope of the investigation. This must be made in a written statement that is mailed or otherwise delivered, to the consumer no later than five days after the date on which the request was received from the consumer or the report was first requested, whichever is later in time.</p>

    </li>
</ul>

<p><br />
</p>

<ol start="4" type="I"><li><p><b>SPECIAL PROCEDURES FOR EMPLOYEE INVESTIGATIONS</b></p>

    </li>
</ol><p>Section 603(x) provides special procedures for investigations of suspected misconduct by an employee or for compliance with Federal, state or local laws and regulations or the rules of a self-regulatory organization, and compliance with written policies of the employer. These investigations are not treated as consumer reports so long as the employer or its agent complies with the procedures set forth in Section 603(x), and a summary describing the nature and scope of the inquiry is made to the employee if an adverse action is taken based on the investigation. </p>

<ol start="4" type="I"><li><p><b>OBLIGATIONS OF USERS OF MEDICAL INFORMATION</b></p>

    </li>
</ol><p>Section 604(g) limits the use of medical information obtained from consumer reporting agencies (other than payment information that appears in a coded form that does not identify the medical provider). If the information is to be used for an insurance transaction, the consumer must give consent to the user of the report or the information must be coded. If the report is to be used for employment purposes - or in connection with a credit transaction (except as provided in federal regulations) - the consumer must provide specific written consent and the medical information must be relevant. Any user who receives medical information shall not disclose the information to any other person (except where necessary to carry out the purpose for which the information was disclosed, or as permitted by statute, regulation, or order).</p>

<ol start="4" type="I"><li><p><b>OBLIGATIONS OF USERS OF "PRESCREENED" LISTS</b></p>

    </li>
</ol><p>The FCRA permits creditors and insurers to obtain limited consumer report information for use in connection with unsolicited offers of credit or insurance under certain circumstances. Sections 603(1), 604(c), 604(e), and 615(d). This practice is known as"prescreening" and typically involves obtaining from a CRA a list of consumers who meet certain pre-established criteria. If any person intends to use prescreened lists, that person must (1) before the offer is made, establish the criteria that will be relied upon to make the offer and grant credit or insurance, and (2) maintain such criteria on file for a three-year period beginning on the date on which the offer is made to each consumer. In addition, any user must provide with each written solicitation a clear and conspicuous statement that:</p>

<ul>
    <li><p>Information contained in a consumer's CRA file was used in connection with the transaction.</p>

    </li>
    <li><p>The consumer received the offer because he or she satisfied the criteria for credit worthiness or insurability used to screen for the offer.</p>

    </li>
    <li><p>Credit or insurance may not be extended if, after the consumer responds, it is determined that the consumer does not meet the criteria used for screening or any applicable criteria bearing on credit worthiness or insurability, or the consumer does not furnish required collateral.</p>

    </li>
    <li><p>The consumer may prohibit the use of information in his or her file in connection with future prescreened offers of credit or insurance by contacting the notification system established by the CRA that provided the report. The statement must include the address and toll-free telephone number of the appropriate notification system.</p>

    </li>
</ul>

<p>In addition, the Consumer Financial Protection Bureau has established the format, type size, and manner of the disclosure required by Section 615(d), with which users must comply. The regulation is 12CFR 1022.54. </p>

<ol start="4" type="I"><li><p><b>OBLIGATIONS OF RESELLERS</b></p>

    </li>
</ol><p><br />
</p>

<ol type="A"><li><p><b>Disclosure and Certification Requirements</b></p>

    </li>
</ol><p>Section 607(e) requires any person who obtains a consumer report for resale to take the following steps:</p>

<ul>
    <li><p>Disclose the identity of the end-user to the source CRA.</p>

    </li>
    <li><p>Identify to the source CRA each permissible purpose for which the report will be furnished to the end-user.</p>

    </li>
    <li><p>Establish and follow reasonable procedures to ensure that reports are resold only for permissible purposes, including procedures to obtain:</p>

    </li>
</ul>

<ol start="2" type="I"><ol type="A"><ol type="i"><ol><li><p>The identity of all end-users;</p>

                </li>
                <li><p>Certifications from all users of each purpose for which reports will be used; and</p>

                </li>
                <li><p>Certifications that reports will not be used for any purpose other than the purpose(s) specified to the reseller. Resellers must make reasonable efforts to verify this information before selling the report.</p>

                </li>
            </ol></ol><li><p><b>Reinvestigations by Resellers</b></p>

        </li>
    </ol></ol><p><br />
</p>

<p>Under Section 611(f), if a consumer disputes the accuracy or completeness of information in a report prepared by a reseller, the reseller must determine whether this is a result of an action or omission on its part and, if so, correct or delete the information. If not, the reseller must send the dispute to the source CRA for reinvestigation. When any CRA notifies the reseller of the results of an investigation, the reseller must immediately convey the information to the consumer. </p>

<p><br />
</p>

<ol start="2" type="I"><ol start="3" type="A"><li><p><b>Fraud Alerts and Resellers</b></p>

        </li>
    </ol></ol><p><br />
</p>

<p>Section 605A(f) requires resellers who receive fraud alerts or active duty alerts from another consumer reporting agency to include these in their reports.</p>

<p><br />
</p>

<ol start="4" type="I"><li><p><b>LIABILITY FOR VIOLATONS OF THE FCRA</b></p>

    </li>
</ol><p>Failure to comply with the FCRA can result in state government or federal government enforcement actions, as well as private lawsuits. Sections 616, 617, and 621. In addition, any person who knowingly and willfully obtains a consumer report under false pretenses may face criminal prosecution. Section 619.</p>

